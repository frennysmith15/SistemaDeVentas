    private void usuariosActivos()
    {
        String sql = "select usuario, cerrosesion from logger_usuarios lu, "
                + "usuarios u where u.codigo = lu.codigoempleado and lu.cerrosesion is null";
        ArrayList<String> usuariosActivos = new ArrayList();
        String usuarios = "\n";
        
        try {
            PreparedStatement ps = ConexionBD.conexionBD.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while(rs.next())
            {
                String usuario = rs.getString(1);
                usuariosActivos.add(usuario);
            }
            
            
            usuarios = usuariosActivos.stream().map(s -> s + "\n").reduce(usuarios, String::concat);
            
            JOptionPane.showMessageDialog(this, "Actualmente est√°n activos: " + usuarios, "Usuarios activos", JOptionPane.INFORMATION_MESSAGE);
            
            rs.close();
            rs = null;
            ps.close();
            ps = null;
            
        } catch (SQLException ex) {
            Logger.getLogger(FrmConsultarEmpleados.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }
    
    
    public void actualizarTabla() {
        
        DefaultTableModel modelo = (DefaultTableModel)tblUsuarios.getModel();
     
        for(int i = 0; i < modelo.getRowCount(); i++)
        {
           modelo.removeRow(i);
           i=i-1;
        }
        
        String sql = "select empleados.codigo, empleados.nombre, empleados.Telefono, empleados.usuario, logger_usuarios.iniciosesion, logger_usuarios.cerrosesion from empleados inner join "
                + "logger_usuarios on logger_usuarios.codigoempleado = empleados.codigo";
        
        try {
            PreparedStatement ps = ConexionBD.conexionBD.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while(rs.next())
            {

                Object celdas[] = new Object[6];
                celdas[0] = rs.getInt(1);
                celdas[1] = rs.getString(2);
                celdas[2] = rs.getString(3);
                celdas[3] = rs.getString(4);
                celdas[4] = rs.getString(5);
                celdas[5] = rs.getString(6);

                modelo.addRow(celdas);
            }
            tblUsuarios.setModel(modelo);

            } catch (SQLException ex) {
                Logger.getLogger(FrmConsultarUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Erro en la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
            }
    }
    


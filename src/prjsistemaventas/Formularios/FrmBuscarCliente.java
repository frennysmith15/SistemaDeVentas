    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        
        if(cliente != null)
        {
            FrmFormularioPrincipal.mantCliente.setTxt(cliente);
            FrmFormularioPrincipal.transVenta.setTxtCliente(cliente);
            this.dispose();
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Debe seleccionar una opcion", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSeleccionarActionPerformed

    
    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        int seleccionar = tblClientes.rowAtPoint(evt.getPoint());
        if(seleccionar != -1)
        {

            int codigo = Integer.valueOf(String.valueOf(tblClientes.getValueAt(seleccionar, 0)));
            String cedula = String.valueOf(tblClientes.getValueAt(seleccionar, 1));
            String nombre = String.valueOf(tblClientes.getValueAt(seleccionar, 2));        
            String direccion = String.valueOf(tblClientes.getValueAt(seleccionar, 3));

            cliente = new Cliente(codigo, cedula, nombre, direccion);
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Debe seleccionar una opcion", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tblClientesMouseClicked


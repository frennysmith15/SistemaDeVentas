    public void actualizarTabla()
    {
       
       DefaultTableModel modelo = (DefaultTableModel)tblClientes.getModel();
       
       
       for(int i = 0; i < modelo.getRowCount(); i++)
       {
           modelo.removeRow(i);
           i=i-1;
       }
           
           
        ArrayList<Cliente> clienteList = new ClientesCRUDImp().mostrarTodos();
        int contador = 0;
        
        for(Cliente c : clienteList)
        {
            
            Object celdas[] = new Object[4];
            celdas[0] = c.getCodigo();
            celdas[1] = c.getCedula();
            celdas[2] = c.getNombres();
            celdas[3] = c.getDireccion();
           
                    
            modelo.addRow(celdas);
            contador++;
        }
        
        
        tblClientes.setModel(modelo);
        
    }
